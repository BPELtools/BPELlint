package isabel.io;

public class DescriptionNotFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6396020760714942253L;

	public DescriptionNotFoundException() {
		super();
	}

	public DescriptionNotFoundException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public DescriptionNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DescriptionNotFoundException(String arg0) {
		super(arg0);
	}

	public DescriptionNotFoundException(Throwable arg0) {
		super(arg0);
	}

}
